public class MateModel {

  private int num1;
  private int num2;
  private String opera;
  private int rpta;

  public MateModel() {
  }

  public int getNum1() {
    return num1;
  }

  public void setNum1(int num1) {
    this.num1 = num1;
  }

  public int getNum2() {
    return num2;
  }

  public void setNum2(int num2) {
    this.num2 = num2;
  }

  public String getOpera() {
    return opera;
  }

  public void setOpera(String opera) {
    this.opera = opera;
  }

  public int getRpta() {
    return rpta;
  }

  public void setRpta(int rpta) {
    this.rpta = rpta;
  }

}
